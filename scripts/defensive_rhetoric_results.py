from __future__ import annotations

import pandas as pd

from scripts.lexicon_scoring import DEFENSIVE_CUES, NEGATIVE_LEXICON, lexicon_label, score_terms
from scripts.paths import PROCESSED, REPORTS

SAMPLE_PATH = PROCESSED / "defensive_rhetoric_sample.parquet"
REPORT_PATH = REPORTS / "defensive_rhetoric_results.md"


def selected_mask(df: pd.DataFrame) -> pd.Series:
    return df["selected"].astype(str).str.lower().isin(["true", "1", "yes"])


def accuracy(df: pd.DataFrame, prediction_col: str) -> float:
    return float((df[prediction_col] == df["tyler_label"]).mean()) if len(df) else 0.0


def percentage_agreement_table(df: pd.DataFrame) -> pd.DataFrame:
    rows = []
    for sub_type, part in df.groupby("sub_type", sort=True):
        rows.append(
            {
                "sub_type": sub_type,
                "lexicon_agreement": f"{accuracy(part, 'lexicon_label'):.0%}",
                "sonnet_agreement": f"{accuracy(part, 'llm_label'):.0%}",
            }
        )
    rows.append(
        {
            "sub_type": "TOTAL",
            "lexicon_agreement": f"{accuracy(df, 'lexicon_label'):.0%}",
            "sonnet_agreement": f"{accuracy(df, 'llm_label'):.0%}",
        }
    )
    return pd.DataFrame(rows)


def label_confusion(df: pd.DataFrame, prediction_col: str) -> pd.DataFrame:
    labels = [
        "pro_immigration_or_pro_refugee",
        "restrictive_or_hostile",
        "mixed_or_unclear",
        "not_immigration_stance",
    ]
    return pd.crosstab(df["tyler_label"], df[prediction_col]).reindex(index=labels, columns=labels, fill_value=0)


def subtype_confusion(df: pd.DataFrame, prediction_col: str) -> pd.DataFrame:
    labels = [
        "pro_immigration_or_pro_refugee",
        "restrictive_or_hostile",
        "mixed_or_unclear",
        "not_immigration_stance",
    ]
    return pd.crosstab(df["sub_type"], df[prediction_col]).reindex(columns=labels, fill_value=0)


def main() -> None:
    sample = pd.read_parquet(SAMPLE_PATH)

    sample["negative_lexicon_score"] = sample["speech_text"].map(lambda text: score_terms(text, NEGATIVE_LEXICON))
    sample["defensive_cue_score"] = sample["speech_text"].map(lambda text: score_terms(text, DEFENSIVE_CUES))
    sample["rough_sentiment"] = sample["defensive_cue_score"] - sample["negative_lexicon_score"]
    sample["lexicon_label"] = sample.apply(
        lambda row: lexicon_label(row["negative_lexicon_score"], row["defensive_cue_score"]),
        axis=1,
    )

    sample.to_parquet(SAMPLE_PATH, index=False)

    both_wrong = sample[(sample["lexicon_label"] != sample["tyler_label"]) & (sample["llm_label"] != sample["tyler_label"])]
    llm_misses = sample[sample["llm_label"] != sample["tyler_label"]]
    lexicon_misses = sample[sample["lexicon_label"] != sample["tyler_label"]]

    report = f"""# Defensive Rhetoric Sample Results

Sample: 47 selected speeches from `data/processed/defensive_rhetoric_sample.parquet`.

Gold labels: `tyler_label`.

Lexicon baseline: exact Tier 1 word list and scoring rule implemented in `scripts/lexicon_scoring.py`, applied to full `speech_text`.

LLM: Sonnet labels in `llm_label`, generated by `scripts/label_defensive_sample_llm.py`.

## Agreement by Sub-Type

Percentage agreement with `tyler_label` by sub-type:

{percentage_agreement_table(sample).to_markdown(index=False)}

## Lexicon Labels by Sub-Type

Counts of the labels assigned by the lexicon baseline:

{subtype_confusion(sample, "lexicon_label").to_markdown()}

## Tyler Label Distribution

{sample["tyler_label"].value_counts().to_frame("n").to_markdown()}

## LLM Label Distribution

{sample["llm_label"].value_counts().to_frame("n").to_markdown()}

## Lexicon Label Distribution

{sample["lexicon_label"].value_counts().to_frame("n").to_markdown()}

## Tyler vs Sonnet

{label_confusion(sample, "llm_label").to_markdown()}

## Tyler vs Lexicon

{label_confusion(sample, "lexicon_label").to_markdown()}

## Cases Where Both Systems Miss Tyler Label

{both_wrong[["speech_id", "date", "speaker", "party", "sub_type", "tyler_label", "lexicon_label", "llm_label", "selection_rationale"]].to_markdown(index=False)}

## Sonnet Misses

{llm_misses[["speech_id", "date", "speaker", "party", "sub_type", "tyler_label", "llm_label", "selection_rationale"]].to_markdown(index=False)}

## Lexicon Misses

{lexicon_misses[["speech_id", "date", "speaker", "party", "sub_type", "tyler_label", "lexicon_label", "negative_lexicon_score", "defensive_cue_score"]].to_markdown(index=False)}

## Short Read

Sonnet matches the hand labels on all 47 selected speeches, including the four defensive/pro-refugee subtypes built around critique, negation, quotation, and refutation. It also tracks the ministerial/control cases: 15 restrictive labels and two `mixed_or_unclear` labels, matching the hand-coded distribution.

The lexicon behaves as expected for this stress test: it is often pulled toward `restrictive_or_hostile` by hostile surface terms in pro/refugee speeches, and toward `pro_immigration_or_pro_refugee` by humanitarian vocabulary in ministerial-restrictive speeches. That makes the sample useful for the claim that surface vocabulary and substantive stance diverge in systematic ways.
"""
    REPORT_PATH.write_text(report)
    print(f"updated {SAMPLE_PATH}")
    print(f"wrote {REPORT_PATH}")
    print(percentage_agreement_table(sample).to_string(index=False))


if __name__ == "__main__":
    main()
